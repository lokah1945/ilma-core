# ============================================================================

def produce_artifact(
    target: str,
    task_class: str = "heavy",
    artifact_type: str = "markdown_report",
    output_dir: str = DEFAULT_OUTPUT_DIR,
) -> ArtifactResult:
    """Standalone function to produce an artifact."""
    producer = DefaultArtifactProducer(output_dir=output_dir)
    return producer.produce_artifact(target, task_class, artifact_type)


def revise_artifact(original_path: str, reflection_feedback: str) -> ArtifactResult:
    """Standalone function to revise an artifact."""
    producer = DefaultArtifactProducer()
    return producer.revise_artifact(original_path, reflection_feedback)


