class DefaultArtifactProducer:
    """
    Produces real artifacts for the autonomous evolution loop.

    Given a target and artifact type, generates content, writes to disk,
    and returns the path for judge evaluation.
    """

    def __init__(self, output_dir: str = DEFAULT_OUTPUT_DIR):
        self.output_dir = Path(output_dir)
        self.output_dir.mkdir(parents=True, exist_ok=True)

        # Ensure safe output dir
        safe_dirs = [Path(d) for d in SAFE_OUTPUT_DIRS]
        if not any(self.output_dir == sd or self.output_dir.is_relative_to(sd) for sd in safe_dirs):
            self.output_dir = Path(DEFAULT_OUTPUT_DIR)
            self.output_dir.mkdir(parents=True, exist_ok=True)

    def produce_artifact(
        self,
        target: str,
        task_class: str = "heavy",
        artifact_type: str = "markdown_report",
        context: Optional[Dict] = None,
        allow_overwrite: bool = False,
    ) -> ArtifactResult:
        """
        Produce an artifact of the specified type.

        Args:
            target: What the artifact should accomplish
            task_class: Classification of task
            artifact_type: Type of artifact to produce
            context: Additional context for content generation
            allow_overwrite: Whether to allow overwriting existing files

        Returns:
            ArtifactResult with path, summary, and errors
        """
        context = context or {}
        errors = []
        warnings = []

        # Validate artifact type
        if artifact_type not in ARTIFACT_TYPES:
            errors.append(f"Unknown artifact type: {artifact_type}")
            return ArtifactResult(
                success=False,
                artifact_path=None,
                artifact_type=artifact_type,
                content_summary="",
                evidence_id="",
                errors=errors,
            )

        # Generate evidence ID
        evidence_id = f"ILMA-EVID-{datetime.now().strftime('%Y%m%d')}-48A-{uuid.uuid4().hex[:8].upper()}"
        context["evidence_id"] = evidence_id

        # Generate content
        template_name = ARTIFACT_TYPES[artifact_type]["template"]
        if template_name in CONTENT_GENERATORS:
            generator = CONTENT_GENERATORS[template_name]
            content = generator(target, task_class, context)
        else:
            content = f"# Artifact: {target}\n\nGenerated: {datetime.now().isoformat()}\n"

        # Determine file path
        ext = ARTIFACT_TYPES[artifact_type]["ext"]
        safe_target = re.sub(r'[^a-zA-Z0-9_-]', '_', target[:50])
        filename = f"artifact_{safe_target}_{datetime.now().strftime('%H%M%S')}{ext}"
        artifact_path = self.output_dir / filename

        # Check for existing file
        if artifact_path.exists() and not allow_overwrite:
            warnings.append(f"File exists: {artifact_path}. Will not overwrite.")
            counter = 1
            while artifact_path.exists():
                filename = f"artifact_{safe_target}_{datetime.now().strftime('%H%M%S')}_{counter}{ext}"
                artifact_path = self.output_dir / filename
                counter += 1
                if counter > 100:
                    errors.append("Too many existing files, cannot create unique path")
                    return ArtifactResult(
                        success=False, artifact_path=None,
                        artifact_type=artifact_type, content_summary="",
                        evidence_id=evidence_id, errors=errors,
                    )

        # Write file
        try:
            artifact_path.write_text(content, encoding='utf-8')
        except Exception as e:
            errors.append(f"Failed to write file: {e}")
            return ArtifactResult(
                success=False, artifact_path=None,
                artifact_type=artifact_type, content_summary="",
                evidence_id=evidence_id, errors=errors,
            )

        # Generate summary
        content_summary = content[:200] + "..." if len(content) > 200 else content

        return ArtifactResult(
            success=True,
            artifact_path=str(artifact_path),
            artifact_type=artifact_type,
            content_summary=content_summary,
            evidence_id=evidence_id,
            errors=errors,
            warnings=warnings,
        )

    def revise_artifact(
        self,
        original_path: str,
        reflection_feedback: str,
        allow_overwrite: bool = True,
    ) -> ArtifactResult:
        """
        Revise an existing artifact based on reflection feedback.

        Args:
            original_path: Path to the artifact to revise
            reflection_feedback: Feedback from reflection engine
            allow_overwrite: Whether to overwrite the original

        Returns:
            ArtifactResult with revised artifact info
        """
        errors = []
        warnings = []

        try:
            original = Path(original_path)
            if not original.exists():
                errors.append(f"Original artifact not found: {original_path}")
                return ArtifactResult(
                    success=False, artifact_path=None,
                    artifact_type="unknown", content_summary="",
                    evidence_id="", errors=errors,
                )

            content = original.read_text(encoding='utf-8')

            # Apply reflection feedback to improve content
            feedback_lower = reflection_feedback.lower()

            # Handle missing evidence/limitations
            if "missing evidence" in feedback_lower or "limitations" in feedback_lower:
                # Add evidence section if missing
                if "## Evidence" not in content and "## Limitations" not in content:
                    evidence_id = f"ILMA-EVID-{datetime.now().strftime('%Y%m%d')}-48A-REV-{uuid.uuid4().hex[:8].upper()}"
                    limitations = """

## Evidence
<!-- Evidence section added during revision -->

## Limitations
<!-- Limitations section added during revision -->
- This artifact was auto-generated
- Claims may not be verified against external sources
- Human review required before production use

---
*Revised by ILMA Default Artifact Producer v1.0*
"""
                    content = content.rstrip() + limitations

            # Handle overclaims
            if "overclaim" in feedback_lower or "guarantee" in feedback_lower:
                # Remove absolute claims
                content = re.sub(r'\b100%\b', 'high', content)
                content = re.sub(r'\bPERFECT\b', 'high-quality', content)
                content = re.sub(r'\bNEVER\b', 'rarely', content)
                content = re.sub(r'\bALWAYS\b', 'in most cases', content)
                content = re.sub(r'\bNO\s*bugs?\b', 'minimal known issues', content)

            # Handle empty/too short
            if len(content) < 200:
                content += "\n\n## Additional Notes\n\nThis artifact was expanded during revision to meet minimum quality standards."

            # Write revised file
            if allow_overwrite:
                original.write_text(content, encoding='utf-8')
                artifact_path = str(original)
            else:
                revised_path = original.with_name(original.stem + "_revised" + original.suffix)
                revised_path.write_text(content, encoding='utf-8')
                artifact_path = str(revised_path)

            evidence_id = f"ILMA-EVID-{datetime.now().strftime('%Y%m%d')}-48A-REV-{uuid.uuid4().hex[:8].upper()}"

            return ArtifactResult(
                success=True,
                artifact_path=artifact_path,
                artifact_type="revised",
                content_summary=content[:200] + "..." if len(content) > 200 else content,
                evidence_id=evidence_id,
                errors=errors,
                warnings=["Artifact revised based on reflection feedback"],
            )

        except Exception as e:
            errors.append(f"Failed to revise artifact: {e}")
            return ArtifactResult(
                success=False, artifact_path=None,
                artifact_type="unknown", content_summary="",
                evidence_id="", errors=errors,
            )


