    def export_rules(self, format: str = 'yara', output_file: str = None) -> str:
        """Export rules in specified format."""
        if format == 'yara':
            content = self.generate_yara_rule_set()
        elif format == 'suricata':
            content = self.generate_suricata_rule_set()
        elif format == 'json':
            content = json.dumps({
                'patterns': [
                    {
                        'id': p.id,
                        'name': p.name,
                        'description': p.description,
                        'pattern': p.pattern,
                        'severity': p.severity,
                        'category': p.category,
                        'mitre_attack': p.mitre_attack
                    } for p in self.threat_patterns
                ]
            }, indent=2)
        else:
            content = ""
        
        if output_file:
            Path(output_file).write_text(content)
            logger.info(f"Exported {len(self.threat_patterns)} rules to {output_file}")
        
        return content
    
    def health_check(self) -> dict:
        """Health endpoint."""
        return {
            "ok": True,
            "module": "blue_team_rules",
            "patterns": len(self.threat_patterns),
            "baselines": len(self.baselines),
            "categories": list(set(p.category for p in self.threat_patterns))
        }

